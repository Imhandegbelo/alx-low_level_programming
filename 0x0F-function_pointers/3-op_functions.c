#include "3-calc.h"

/**
 * op_add - performs addition
 * @a: input one
 * @b: input two
 * Return: 0
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - performs subtraction
 * @a: input one
 * @b: input two
 * Return: 0
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - performs multiplication
 * @a: input one
 * @b: input two
 * Return: 0
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - performs division
 * @a: input one
 * @b: input two
 * Return: 0
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - performs reminder division
 * @a: input one
 * @b: input two
 * Return: 0
 */

int op_mod(int a, int b)
{
	return (a % b);
}
